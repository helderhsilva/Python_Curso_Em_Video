n1 = float(input('Informe a primeira nota do aluno: '))
n2 = float(input('Informe a segunda nota do aluno: '))

media = (n1 + n2)/2

print('\nPrimeira nota: {:.1f}'.format(n1))
print('Segunda nota: {:.1f}'.format(n2))
print('Média do aluno: {:.1f}'.format(media))

